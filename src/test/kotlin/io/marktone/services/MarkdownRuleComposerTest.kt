    // --- Profile parity: both profiles share core structure ---

    @Test
    fun bothProfilesContainAllCoreSelectors() {
        val coreSelectors = listOf(
            "html, body", "h1, h2, h3, h4, h5, h6", "p, li",
            "strong, b", "a {", "blockquote", "code {", "pre {",
            "pre code", "table {", "th {", "td {", "hr {",
        )
        for (profile in MarkToneProfile.entries) {
            val css = composer.compose(tokens, profile)
            coreSelectors.forEach { selector ->
                assertContains(css, selector, message = "$profile missing selector: $selector")
            }
        }
    }

    @Test
    fun bothProfilesEmbedAllTokens() {
        for (profile in MarkToneProfile.entries) {
            val css = composer.compose(tokens, profile)
            tokens.forEach { (key, value) ->
                assertContains(css, "$key: $value;", message = "$profile missing token: $key")
            }
        }
    }

    @Test
    fun bothProfilesSetLineHeight() {
        for (profile in MarkToneProfile.entries) {
            val css = composer.compose(tokens, profile)
            assertContains(css, "--mt-line-height:", message = "$profile missing line-height")
        }
    }

    // --- Edge case: empty or minimal tokens ---

    @Test
    fun emptyTokensStillProducesValidCssStructure() {
        val css = composer.compose(emptyMap(), MarkToneProfile.MINIMAL)
        assertContains(css, ":root")
        assertContains(css, "html, body")
    }

    @Test
    fun singleTokenIsEmbedded() {
        val singleToken = mapOf("--mt-bg" to "#000000")
        val css = composer.compose(singleToken, MarkToneProfile.EXPRESSIVE)
        assertContains(css, "--mt-bg: #000000;")
    }

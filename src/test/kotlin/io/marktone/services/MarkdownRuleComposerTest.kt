    @Test
    fun preCodeHasTransparentBackground() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        assertContains(css, "pre code")
        assertContains(css, "background: transparent")
    }

    @Test
    fun containsTableSelectors() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        assertContains(css, "table {")
        assertContains(css, "th {")
        assertContains(css, "td {")
        assertContains(css, "border-collapse: collapse")
    }

    @Test
    fun containsHorizontalRuleSelector() {

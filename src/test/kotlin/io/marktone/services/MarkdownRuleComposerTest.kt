package io.marktone.services

import io.marktone.settings.MarkToneProfile
import kotlin.test.Test
import kotlin.test.assertContains
import kotlin.test.assertFalse
import kotlin.test.assertTrue

class MarkdownRuleComposerTest {

    private val tokens = linkedMapOf(
        "--mt-font-family" to "\"JetBrains Mono\", sans-serif",
        "--mt-font-size" to "14px",
        "--mt-bg" to "#1e1e1e",
        "--mt-fg" to "#d4d4d4",
        "--mt-muted" to "#6a9955",
        "--mt-link" to "#4ea1ff",
        "--mt-border" to "#3a3a3a",
        "--mt-selection" to "#264f78",
        "--mt-code-bg" to "#2a2d2e",
        "--mt-code-fg" to "#ce9178",
        "--mt-keyword" to "#569cd6",
        "--mt-string" to "#ce9178",
        "--mt-accent" to "#b5cea8",
        "--mt-function" to "#dcdcaa",
    )

    private val composer = MarkdownRuleComposer()

    // --- Root block and token embedding ---

    @Test
    fun outputContainsRootBlock() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        assertContains(css, ":root")
    }

    @Test
    fun rootBlockContainsAllTokenVariables() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        tokens.forEach { (key, value) ->
            assertContains(css, "$key: $value;", message = "Missing token: $key")
        }
    }

    @Test
    fun outputContainsGeneratedByComment() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        assertContains(css, "Generated by MarkTone")
    }

    // --- Core CSS selectors ---

    @Test
    fun containsBodySelector() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        assertContains(css, "html, body")
    }

    @Test
    fun bodyUsesThemeTokens() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        assertContains(css, "background: var(--mt-bg)")
        assertContains(css, "color: var(--mt-fg)")
        assertContains(css, "font-family: var(--mt-font-family)")
        assertContains(css, "font-size: var(--mt-font-size)")
    }

    @Test
    fun containsHeadingSelectors() {
        val css = composer.compose(tokens, MarkToneProfile.EXPRESSIVE)
        assertContains(css, "h1, h2, h3, h4, h5, h6")
        assertContains(css, "h1 {")
        assertContains(css, "h2 {")
        assertContains(css, "h3 {")
